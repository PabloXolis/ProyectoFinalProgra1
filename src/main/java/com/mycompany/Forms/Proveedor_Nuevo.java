    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            // Comprobando que los campos no esten vacíos.
            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()) {
                throw new IllegalArgumentException("Los campos no pueden estar vacíos.");
            }else{
                
                String nombre = jTextField1.getText();
                String nit = jTextField2.getText();
                String direccion = jTextField3.getText();
                String telefono = jTextField4.getText();
               
                Proveedor p = new Proveedor();

                p.nombre = nombre;
                p.nit = nit;
                p.direccion = direccion;
                p.telefono = telefono;
                
                p.activo = true;

                Proyecto_Final_Log_In.proveedores.add(p);
                JOptionPane.showMessageDialog(this, "Nuevo proveedor creado con éxito.");
                       
                
            }
        }catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
                               
    }//GEN-LAST:event_jButton2ActionPerformed


