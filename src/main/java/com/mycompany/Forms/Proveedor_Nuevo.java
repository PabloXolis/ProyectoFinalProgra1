    private Date pasarAF5echa(String fechaStr){
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        try{
            return sdf.parse(fechaStr);
        } catch (ParseException e){
            return null;
        }
    }
    
