    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            // Comprobando que los campos no esten vacíos.
            if (Stream.of(jTextField1, jTextField2, jTextField3, jTextField4, jTextField5, jTextField6)
             .anyMatch(field -> field.getText().trim().isEmpty())) {
                throw new IllegalArgumentException("Los campos no pueden estar vacíos o contener solo espacios.");
            }

            double pCompra;
            double pVenta;
            int quantity;
            
            try {
                pCompra = Double.parseDouble(jTextField3.getText());
                pVenta = Double.parseDouble(jTextField4.getText()); 
                if (pCompra <= 0 || pVenta <= 0) {
                    throw new IllegalArgumentException("Los precios deben ser valores positivos mayores que cero.");
                }
            } catch (NumberFormatException e) {
                throw new NumberFormatException("Los precios deben ser valores numéricos reales.");
            }
            
            try{
                quantity = Integer.parseInt(jTextField6.getText());
                if (quantity <= 0) {
                    throw new IllegalArgumentException("La cantidad deber ser un valor positivo mayor que cero.");
                }
            } catch (NumberFormatException e) {
                throw new NumberFormatException("La cantidad deber ser un valor numérico natural.");
            }
            
            if(pVenta <= pCompra){
                throw new IllegalArgumentException("El precio de venta no pueder ser menor o igual que el de compra ya que tendrá pérdidas.");
            }
                
            Libro l = new Libro();
            l.setTitulo(jTextField1.getText());
            l.setAutor(jTextField2.getText());
            l.setPrecio_compra(pCompra);
            l.setPrecio_venta(pVenta);
            l.setGenero(jTextField5.getText());
            l.setCantidad(quantity);
            l.setActivo(true);
            
            Proyecto_Final_Log_In.libros.add(l);
            JOptionPane.showMessageDialog(this, "Nuevo libro creado con éxito.");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error en formato: " + ex.getMessage());
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex);
        }                      
    }//GEN-LAST:event_jButton2ActionPerformed

