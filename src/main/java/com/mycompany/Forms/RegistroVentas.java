    private void VenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VenderActionPerformed
        boolean vender = true;
        try{
            if (Stream.of(jTextField1, jTextField2, jTextField3)
                .anyMatch(field -> field.getText().trim().isEmpty())) {
                vender = false;
               throw new IllegalArgumentException("Los campos del cliente no pueden estar vac√≠os o contener solo espacios.");
            }
            
            String nit = jTextField1.getText();
            
            try{
                int numeroNit = Integer.parseInt(jTextField1.getText());
                
                if (nit.length() < 9 || nit.length() > 9){
                    vender = false;
                    throw new IllegalArgumentException("El NIT debe ser de nueve d√≠gitos.");
                }
            } catch (NumberFormatException e) {
                vender = false;
                throw new NumberFormatException("El NIT deber ser un valor num√©rico natural.");
            }
            
            if (vender){
                if (jTable1.getRowCount() > 0){
                    Venta venta = new Venta();

                    venta.setTotal(total);
                    venta.setNit(nit);
                    venta.setNombre(jTextField2.getText());
                    venta.setDireccion(jTextField3.getText());
                    venta.setLibrosVendidos(librosSeleccionados);
                    venta.setTotalSinIVA(venta.getTotal() / 1.12);
                    venta.setVendedor(VentanaVendedor.usuarioActual.getNombre());

                    SimpleDateFormat formatoFecha = new SimpleDateFormat(
                        "EEEE, d 'de' MMMM 'de' yyyy, HH:mm:ss",
                        Locale.of("es", "ES")
                    );
                    String fechaHoraFormateada = formatoFecha.format(venta.getFechaYHora().getTime());

                    venta.setActivo(true);

                    Proyecto_Final_Log_In.ventas.add(venta);

                    JOptionPane.showMessageDialog(this, "Venta realizada por " + venta.getVendedor() + " con √©xito, con fecha y hora: " + fechaHoraFormateada + "."
                                                        + "\nCliente: " + venta.getNombre()
                                                        + "\nNIT: " + venta.getNit()
                                                        + "\nTotal sin IVA: " + venta.getTotalSinIVA()
                                                        + "\nTotal con IVA: " + venta.getTotal());
                    
                    for (int i = 0; i < Proyecto_Final_Log_In.libros.size(); i++) {
                        
                        int stock = Proyecto_Final_Log_In.libros.get(i).getCantidad();
                        
                        for (Libro_Vendido libroSeleccionado : librosSeleccionados) {
                            
                            if(libroSeleccionado.getLibro().equals(Proyecto_Final_Log_In.libros.get(i).getTitulo())){
                                Proyecto_Final_Log_In.libros.get(i).setCantidad(cantidadTemporal.get(Proyecto_Final_Log_In.libros.get(i).getTitulo()));
                            }
                        }
                        
                    }
                    
                    
                    
                    //Limpiando los campos de texto.
                    Stream.of(jTextField1, jTextField2, jTextField3, jTextField4)
                    .forEach(field -> field.setText(""));
                    
                    //Limpiando array list
                    librosSeleccionados.clear();
                    
                    //Repintando jtable.
                    pintar_tabla();
                    
                    total = 0;
                    jLabel2.setText(String.valueOf(total));
                }
                else{
                    JOptionPane.showMessageDialog(this, "No has a√±adido nada para vender.");
                }
            }
        }
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error en formato: " + ex.getMessage());
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex);
        } 
    }//GEN-LAST:event_VenderActionPerformed

