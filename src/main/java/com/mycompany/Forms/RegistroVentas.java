    private void pintar_tabla(){
                
            String encabezado [] = {"Libro", "Cantidad", "Subtotal"};

            // Crear modelo con columnas, pero sin filas
            DefaultTableModel t = new DefaultTableModel(encabezado, 0);
            jTable1.setModel(t);

            TableModel tabla = jTable1.getModel();

            for (Libro_Vendido l : librosSeleccionados) {
                Object[] fila = {l.getLibro(), l.getCantidad(), l.getSubtotal()};
                t.addRow(fila);
            }
        }
    
